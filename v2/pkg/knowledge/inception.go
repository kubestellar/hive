const (
	inceptionStateFile  = "inception/state.json"
	ideaDefaultConf     = 0.3
	visionDefaultConf   = 0.6
	constDefaultConf    = 0.9
	reqDefaultConf      = 0.6
	constraintDefaultConf = 0.7
	stakeholderDefaultConf = 0.7
	acceptanceDefaultConf  = 0.6
	brownfieldConfBoost = 0.15
)

// InceptionEngine manages the Level 1 ideation workflow — from raw idea to
// structured KB facts and scaffold output. Supports both greenfield (new
// project) and brownfield (existing repo) modes.
type InceptionEngine struct {
	dataDir string
	api     *KnowledgeAPI
	mu      sync.Mutex
	state   *InceptionState
	logger  *slog.Logger
}

// NewInceptionEngine creates an engine that persists state under dataDir.
func NewInceptionEngine(dataDir string, api *KnowledgeAPI, logger *slog.Logger) *InceptionEngine {
	e := &InceptionEngine{
		dataDir: dataDir,
		api:     api,
		logger:  logger,
	}
	e.loadState()
	e.connectExistingVault()
	return e
}

// Start begins a greenfield inception — the user has an idea but no repo.
func (e *InceptionEngine) Start(rawIdea string) (*InceptionState, error) {
	e.mu.Lock()
	defer e.mu.Unlock()

	rawIdea = strings.TrimSpace(rawIdea)
	if rawIdea == "" {
		return nil, fmt.Errorf("idea text is required")
	}

	if e.state != nil && e.state.Phase != PhaseComplete {
		return nil, fmt.Errorf("inception already in progress (phase: %s) — reset first", e.state.Phase)
	}

	slug := slugify("idea-" + truncateSlug(rawIdea))

	if e.api != nil {
		ctx := context.Background()
		err := e.api.CreateFact(ctx, CreateFactRequest{
			Title:      rawIdea,
			Body:       rawIdea,
			Type:       string(FactIdea),
			Tags:       []string{"inception", "seed"},
			Layer:      string(LayerProject),
			Confidence: ideaDefaultConf,
		})
		if err != nil {
			e.logger.Warn("could not persist idea fact to KB layer (knowledge may not be configured)", "error", err)
		}
	}

	e.clearWikiVault()

	e.state = &InceptionState{
		Phase:     PhaseCapture,
		Mode:      InceptionGreenfield,
		IdeaText:  rawIdea,
		IdeaSlug:  slug,
		FactSlugs: []string{slug},
		Answers:   make(map[string]string),
		StartedAt: time.Now(),
	}

	if err := e.saveState(); err != nil {
		return nil, fmt.Errorf("persisting state: %w", err)
	}

	e.logger.Info("inception started",
		"mode", "greenfield",
		"slug", slug,
	)

	return e.state, nil
}

// StartBrownfield begins an inception against an existing repo. The guide
// agent will scan the repo and extract initial facts.
func (e *InceptionEngine) StartBrownfield(repoURL string) (*InceptionState, error) {
	e.mu.Lock()
	defer e.mu.Unlock()

	if repoURL == "" {
		return nil, fmt.Errorf("repo URL is required")
	}
	if !strings.HasPrefix(repoURL, "https://") && !strings.HasPrefix(repoURL, "http://") {
		return nil, fmt.Errorf("repo URL must start with https:// or http://")
	}

	if e.state != nil && e.state.Phase != PhaseComplete {
		return nil, fmt.Errorf("inception already in progress (phase: %s) — reset first", e.state.Phase)
	}

	slug := slugify("scan-" + repoBaseName(repoURL))

	e.clearWikiVault()

	e.state = &InceptionState{
		Phase:     PhaseCapture,
		Mode:      InceptionBrownfield,
		IdeaSlug:  slug,
		RepoURL:   repoURL,
		FactSlugs: []string{},
		Answers:   make(map[string]string),
		StartedAt: time.Now(),
	}

	if err := e.saveState(); err != nil {
		return nil, fmt.Errorf("persisting state: %w", err)
	}

	e.logger.Info("inception started",
		"mode", "brownfield",
		"repo", repoURL,
		"slug", slug,
	)

	return e.state, nil
}

// SetQuestions records the clarification questions generated by the guide agent
// and advances the state to the clarify phase.
func (e *InceptionEngine) SetQuestions(questions []Question) error {
	e.mu.Lock()
	defer e.mu.Unlock()

	if e.state == nil {
		return fmt.Errorf("no inception in progress")
	}

	if len(questions) == 0 {
		return fmt.Errorf("at least one question is required")
	}

	seen := make(map[string]bool, len(questions))
	for _, q := range questions {
		if q.ID == "" {
			return fmt.Errorf("question ID is required")
		}
		if q.Text == "" {
			return fmt.Errorf("question text is required for %q", q.ID)
		}
		if seen[q.ID] {
			return fmt.Errorf("duplicate question ID: %q", q.ID)
		}
		seen[q.ID] = true
	}

	e.state.Questions = questions
	e.state.Phase = PhaseClarify

	return e.saveState()
}

// SubmitAnswers records user responses to clarification questions and advances
// to the structure phase. The scheduler will re-kick the guide agent with
// the answers injected.
func (e *InceptionEngine) SubmitAnswers(answers map[string]string) (*InceptionState, error) {
	e.mu.Lock()
	defer e.mu.Unlock()

	if e.state == nil {
		return nil, fmt.Errorf("no inception in progress")
	}
	// Accept answers in clarify or structure phase — the brainstorm agent
	// can advance past clarify before the user submits, but the answers
	// are still valuable context for the next kick.
	if e.state.Phase != PhaseClarify && e.state.Phase != PhaseStructure {
		return nil, fmt.Errorf("cannot submit answers in phase %s", e.state.Phase)
	}

	// Validate answer keys match known question IDs
	if len(e.state.Questions) > 0 {
		qIDs := make(map[string]bool, len(e.state.Questions))
		for _, q := range e.state.Questions {
			qIDs[q.ID] = true
		}
		for id, val := range answers {
			if !qIDs[id] {
				return nil, fmt.Errorf("answer key %q does not match any question ID", id)
			}
			if strings.TrimSpace(val) == "" {
				return nil, fmt.Errorf("answer for %q is empty", id)
			}
		}
	}

	e.state.Answers = answers
	if e.state.Phase == PhaseClarify {
		e.state.Phase = PhaseStructure
		e.state.PhaseChangedAt = time.Now()
	}

	if err := e.saveState(); err != nil {
		return nil, fmt.Errorf("persisting state: %w", err)
	}

	e.logger.Info("inception answers submitted",
		"answer_count", len(answers),
		"mode", e.state.Mode,
	)

	return e.state, nil
}

// RecordFacts records structured facts produced by the guide agent during the
// structure phase. Each fact is ingested into the project KB layer.
func (e *InceptionEngine) RecordFacts(ctx context.Context, facts []IdeationFact) error {
	e.mu.Lock()
	defer e.mu.Unlock()

	if e.state == nil {
		return fmt.Errorf("no inception in progress")
	}

	if len(facts) == 0 {
		return fmt.Errorf("at least one fact is required")
	}

	for _, f := range facts {
		if !f.Type.IsIdeation() {
			return fmt.Errorf("invalid ideation fact type: %q", f.Type)
		}
		if f.Title == "" {
			return fmt.Errorf("fact title is required")
		}
		if f.Body == "" {
			return fmt.Errorf("fact body is required for type %q", f.Type)
		}
		conf := defaultConfidence(f.Type)
		if e.state.Mode == InceptionBrownfield {
			conf += brownfieldConfBoost
			if conf > 1.0 {
				conf = 1.0
			}
		}

		if e.api == nil {
			slug := slugify(string(f.Type) + "-" + truncateSlug(f.Title))
			e.state.FactSlugs = append(e.state.FactSlugs, slug)
			continue
		}

		err := e.api.CreateFact(ctx, CreateFactRequest{
			Title:      f.Title,
			Body:       f.Body,
			Type:       string(f.Type),
			Tags:       append(f.Tags, "inception"),
			Layer:      string(LayerProject),
			Confidence: conf,
		})
		if err != nil {
			e.logger.Warn("failed to record ideation fact",
				"title", f.Title,
				"type", f.Type,
				"error", err,
			)
			continue
		}

		slug := slugify(string(f.Type) + "-" + truncateSlug(f.Title))
		e.state.FactSlugs = append(e.state.FactSlugs, slug)
	}

	e.state.Phase = PhaseScaffold

	e.writeFactsToVault(facts)

	return e.saveState()
}

const inceptionWikiDir = "inception-wiki"

func (e *InceptionEngine) clearWikiVault() {
	vaultDir := filepath.Join(e.dataDir, inceptionWikiDir)
	entries, err := os.ReadDir(vaultDir)
	if err != nil {
		return
	}
	for _, entry := range entries {
		os.Remove(filepath.Join(vaultDir, entry.Name()))
	}
	if len(entries) > 0 {
		e.logger.Info("inception wiki cleared on new start", "files", len(entries))
	}
}

// writeFactsToVault writes each fact as a markdown file with YAML frontmatter
// into a local directory, then connects it as a KB vault so facts are available
// to all agents via ${KNOWLEDGE} priming.
func (e *InceptionEngine) writeFactsToVault(facts []IdeationFact) {
	vaultDir := filepath.Join(e.dataDir, inceptionWikiDir)
	if err := os.MkdirAll(vaultDir, 0o755); err != nil {
		e.logger.Warn("failed to create inception wiki dir", "error", err)
		return
	}

	// Remove stale wiki files from previous inception before writing new ones.
	if len(facts) > 0 {
		if entries, err := os.ReadDir(vaultDir); err == nil && len(entries) > 0 {
			for _, entry := range entries {
				os.Remove(filepath.Join(vaultDir, entry.Name()))
			}
			e.logger.Info("inception wiki cleared before write", "files", len(entries), "new_facts", len(facts))
		}
	}

	for _, f := range facts {
		slug := slugify(string(f.Type) + "-" + truncateSlug(f.Title))
		filename := slug + ".md"

		conf := defaultConfidence(f.Type)
		tags := append(f.Tags, "inception", string(f.Type))

		var content strings.Builder
		content.WriteString("---\n")
		fmt.Fprintf(&content, "title: %s\n", f.Title)
		fmt.Fprintf(&content, "type: %s\n", string(f.Type))
		fmt.Fprintf(&content, "confidence: %.2f\n", conf)
		if len(tags) > 0 {
			fmt.Fprintf(&content, "tags: [%s]\n", strings.Join(tags, ", "))
		}
		content.WriteString("---\n\n")
		content.WriteString(f.Body)

		path := filepath.Join(vaultDir, filename)
		if err := os.WriteFile(path, []byte(content.String()), 0o644); err != nil {
			e.logger.Warn("failed to write inception fact file", "path", path, "error", err)
			continue
		}
	}

	if e.api != nil {
		if err := e.api.ConnectVault(vaultDir, "inception-wiki"); err != nil {
			if strings.Contains(err.Error(), "already connected") {
				// Vault exists — reindex to pick up new files
				if err := e.api.ReindexVault(vaultDir); err != nil {
					e.logger.Warn("failed to reindex inception wiki", "error", err)
				} else {
					e.logger.Info("inception wiki reindexed", "dir", vaultDir, "facts", len(facts))
				}
			} else {
				e.logger.Warn("failed to connect inception wiki", "error", err)
			}
		} else {
			e.logger.Info("inception wiki connected as knowledge source",
				"dir", vaultDir,
				"facts", len(facts),
			)
		}
	}
}

// connectExistingVault checks if inception facts were previously written to disk
// and reconnects the vault on startup (survives container restarts).
func (e *InceptionEngine) connectExistingVault() {
	if e.api == nil {
		return
	}
	vaultDir := filepath.Join(e.dataDir, inceptionWikiDir)
	entries, err := os.ReadDir(vaultDir)
	if err != nil || len(entries) == 0 {
		return
	}
	wikiName := "inception-wiki"
	if e.state != nil && e.state.WikiName != "" {
		wikiName = e.state.WikiName
	}
	if err := e.api.ConnectVault(vaultDir, wikiName); err != nil {
		if !strings.Contains(err.Error(), "already connected") {
			e.logger.Warn("failed to reconnect inception wiki on startup", "error", err)
		}
	} else {
		e.logger.Info("inception wiki reconnected on startup",
			"dir", vaultDir,
			"name", wikiName,
			"files", len(entries),
		)
	}
}

// IdeationFact is the input for recording a new ideation fact from the guide agent.
type IdeationFact struct {
	Title string   `json:"title"`
	Body  string   `json:"body"`
	Type  FactType `json:"type"`
	Tags  []string `json:"tags"`
}

// ProduceScaffold generates bootstrap files from the structured facts. For
// greenfield, this is a full project skeleton. For brownfield, only missing
// files are generated.
func (e *InceptionEngine) ProduceScaffold(ctx context.Context) (*ScaffoldResult, error) {
	e.mu.Lock()
	defer e.mu.Unlock()

	if e.state == nil {
		return nil, fmt.Errorf("no inception in progress")
	}

	facts := e.gatherFacts(ctx)

	result := &ScaffoldResult{}

	vision := findFactByType(facts, FactVision)
	constitution := findFactByType(facts, FactConstitution)
	requirements := filterFactsByType(facts, FactRequirement)
	constraints := filterFactsByType(facts, FactConstraint)
	acceptance := filterFactsByType(facts, FactAcceptance)
	stakeholders := filterFactsByType(facts, FactStakeholder)

	lang := inferLanguage(constitution)
	projectName := inferProjectName(vision, e.state)

	// Core documentation
	result.Files = append(result.Files, ScaffoldFile{
		Path:    "README.md",
		Content: buildReadme(e.state.IdeaText, vision, constitution, requirements, constraints, stakeholders),
		Purpose: "readme",
		IsNew:   true,
	})

	result.Files = append(result.Files, ScaffoldFile{
		Path:    "AGENTS.md",
		Content: buildAgentsMD(constitution, constraints, requirements, vision),
		Purpose: "agents_md",
		IsNew:   true,
	})

	result.Files = append(result.Files, ScaffoldFile{
		Path:    "CONTRIBUTING.md",
		Content: buildContributing(constitution),
		Purpose: "contributing",
		IsNew:   true,
	})

	// CI/CD — multi-tiered: PR checks, nightly full suite
	result.Files = append(result.Files, ScaffoldFile{
		Path:    ".github/workflows/ci.yml",
		Content: buildCIConfig(constitution),
		Purpose: "ci",
		IsNew:   true,
	})
	result.Files = append(result.Files, ScaffoldFile{
		Path:    ".github/workflows/nightly.yml",
		Content: buildNightlyCI(lang),
		Purpose: "nightly_ci",
		IsNew:   true,
	})

	// Git
	result.Files = append(result.Files, ScaffoldFile{
		Path:    ".gitignore",
		Content: buildGitignore(lang),
		Purpose: "gitignore",
		IsNew:   true,
	})

	// Language-specific project files + code stubs
	switch lang {
	case "go":
		result.Files = append(result.Files,
			ScaffoldFile{Path: "go.mod", Content: buildGoMod(projectName), Purpose: "go_mod", IsNew: true},
			ScaffoldFile{Path: "main.go", Content: buildGoMain(projectName, vision), Purpose: "main", IsNew: true},
			ScaffoldFile{Path: "cmd/root.go", Content: buildGoCmdRoot(projectName, vision), Purpose: "cmd", IsNew: true},
		)
		if len(acceptance) > 0 {
			result.Files = append(result.Files, ScaffoldFile{
				Path: "main_test.go", Content: buildTestStubs(acceptance, constitution), Purpose: "test_stub", IsNew: true,
			})
		}
	case "python":
		result.Files = append(result.Files,
			ScaffoldFile{Path: "pyproject.toml", Content: buildPyprojectToml(projectName, vision), Purpose: "pyproject", IsNew: true},
			ScaffoldFile{Path: "src/" + projectName + "/__init__.py", Content: buildPyInit(projectName, vision), Purpose: "main", IsNew: true},
			ScaffoldFile{Path: "src/" + projectName + "/cli.py", Content: buildPyCLI(projectName, vision), Purpose: "cmd", IsNew: true},
		)
		if len(acceptance) > 0 {
			result.Files = append(result.Files, ScaffoldFile{
				Path: "tests/test_acceptance.py", Content: buildTestStubs(acceptance, constitution), Purpose: "test_stub", IsNew: true,
			})
		}
	case "typescript", "javascript":
		result.Files = append(result.Files,
			ScaffoldFile{Path: "package.json", Content: buildPackageJSON(projectName, vision), Purpose: "package_json", IsNew: true},
			ScaffoldFile{Path: "tsconfig.json", Content: buildTSConfig(), Purpose: "tsconfig", IsNew: true},
			ScaffoldFile{Path: "src/index.ts", Content: buildTSIndex(projectName, vision), Purpose: "main", IsNew: true},
		)
		if len(acceptance) > 0 {
			result.Files = append(result.Files, ScaffoldFile{
				Path: "src/__tests__/acceptance.test.ts", Content: buildTestStubs(acceptance, constitution), Purpose: "test_stub", IsNew: true,
			})
		}
	case "rust":
		result.Files = append(result.Files,
			ScaffoldFile{Path: "Cargo.toml", Content: buildCargoToml(projectName, vision), Purpose: "cargo_toml", IsNew: true},
			ScaffoldFile{Path: "src/main.rs", Content: buildRustMain(projectName, vision), Purpose: "main", IsNew: true},
		)
		if len(acceptance) > 0 {
			result.Files = append(result.Files, ScaffoldFile{
				Path: "tests/acceptance.rs", Content: buildTestStubs(acceptance, constitution), Purpose: "test_stub", IsNew: true,
			})
		}
	case "java":
		result.Files = append(result.Files,
			ScaffoldFile{Path: "pom.xml", Content: buildPomXml(projectName, vision), Purpose: "pom", IsNew: true},
			ScaffoldFile{Path: "src/main/java/App.java", Content: buildJavaMain(projectName, vision), Purpose: "main", IsNew: true},
		)
		if len(acceptance) > 0 {
			result.Files = append(result.Files, ScaffoldFile{
				Path: "src/test/java/AppTest.java", Content: buildTestStubs(acceptance, constitution), Purpose: "test_stub", IsNew: true,
			})
		}
	case "shell":
		result.Files = append(result.Files,
			ScaffoldFile{Path: projectName + ".sh", Content: buildShellMain(projectName, vision), Purpose: "main", IsNew: true},
		)
		if len(acceptance) > 0 {
			result.Files = append(result.Files, ScaffoldFile{
				Path: "test.sh", Content: buildShellTestStubs(acceptance), Purpose: "test_stub", IsNew: true,
			})
		}
	}

	// Makefile
	result.Files = append(result.Files, ScaffoldFile{
		Path:    "Makefile",
		Content: buildMakefile(lang, projectName),
		Purpose: "makefile",
		IsNew:   true,
	})

	// Project-type-specific files
	projectType := inferProjectType(constitution, requirements, vision)
	if projectType == "container" || projectType == "kubernetes" {
		result.Files = append(result.Files, ScaffoldFile{
			Path: "Dockerfile", Content: buildDockerfile(lang, projectName), Purpose: "dockerfile", IsNew: true,
		})
	}

	if projectType == "ui" {
		result.Files = append(result.Files,
			ScaffoldFile{Path: "src/i18n/en.json", Content: buildI18nFile("en", projectName), Purpose: "i18n", IsNew: true},
			ScaffoldFile{Path: "src/i18n/es.json", Content: buildI18nFile("es", projectName), Purpose: "i18n", IsNew: true},
			ScaffoldFile{Path: "src/i18n/index.ts", Content: buildI18nIndex(), Purpose: "i18n", IsNew: true},
		)
	}

	if projectType == "kubernetes" {
		result.Files = append(result.Files,
			ScaffoldFile{Path: "deploy/kustomization.yaml", Content: buildKustomization(projectName), Purpose: "kustomize", IsNew: true},
			ScaffoldFile{Path: "deploy/base/deployment.yaml", Content: buildK8sDeployment(projectName), Purpose: "k8s_deployment", IsNew: true},
			ScaffoldFile{Path: "deploy/base/service.yaml", Content: buildK8sService(projectName), Purpose: "k8s_service", IsNew: true},
			ScaffoldFile{Path: "deploy/base/configmap.yaml", Content: buildK8sConfigMap(projectName), Purpose: "k8s_configmap", IsNew: true},
			ScaffoldFile{Path: "deploy/base/secret.yaml", Content: buildK8sSecret(projectName), Purpose: "k8s_secret", IsNew: true},
			ScaffoldFile{Path: "deploy/base/kustomization.yaml", Content: buildK8sBaseKustomization(projectName), Purpose: "kustomize_base", IsNew: true},
			ScaffoldFile{Path: "deploy/overlays/dev/kustomization.yaml", Content: buildK8sOverlayKustomization(projectName, "dev"), Purpose: "kustomize_dev", IsNew: true},
			ScaffoldFile{Path: "deploy/overlays/prod/kustomization.yaml", Content: buildK8sOverlayKustomization(projectName, "prod"), Purpose: "kustomize_prod", IsNew: true},
		)
	}

	return result, nil
}

// AdvanceToComplete marks the inception as complete (scaffold approved).
func (e *InceptionEngine) AdvanceToComplete() error {
	e.mu.Lock()
	defer e.mu.Unlock()

	if e.state == nil {
		return fmt.Errorf("no inception in progress")
	}
	if e.state.Phase != PhaseScaffold && e.state.Phase != PhaseComplete {
		return fmt.Errorf("cannot approve in phase %s — must be in scaffold phase", e.state.Phase)
	}

	e.state.Phase = PhaseComplete
	e.logger.Info("inception complete",
		"mode", e.state.Mode,
		"facts", len(e.state.FactSlugs),
	)
	return e.saveState()
}

// GetState returns the current inception state, or nil if none is in progress.
func (e *InceptionEngine) GetState() *InceptionState {
	e.mu.Lock()
	defer e.mu.Unlock()
	return e.state
}

// ReadIdeaFact returns the seed idea fact for the current inception.
func (e *InceptionEngine) ReadIdeaFact(ctx context.Context) (*Fact, error) {
	e.mu.Lock()
	defer e.mu.Unlock()

	if e.state == nil {
		return nil, fmt.Errorf("no inception in progress")
	}
	if e.api == nil {
		return nil, nil
	}
	return e.api.ReadFact(ctx, e.state.IdeaSlug)
}

// Reset clears the inception state so the user can start over.
// SetWikiName persists a vanity name for the inception wiki.
func (e *InceptionEngine) SetWikiName(name string) {
	e.mu.Lock()
	defer e.mu.Unlock()
	if e.state != nil {
		e.state.WikiName = name
		e.saveState()
	}
}

// HasWikiFiles returns true if the inception wiki has any files from a previous run.
func (e *InceptionEngine) HasWikiFiles() bool {
	wikiDir := filepath.Join(e.dataDir, inceptionWikiDir)
	entries, err := os.ReadDir(wikiDir)
	if err != nil {
		return false
	}
	for _, entry := range entries {
		if strings.HasSuffix(entry.Name(), ".md") {
			return true
		}
	}
	return false
}

func (e *InceptionEngine) Reset() error {
	e.mu.Lock()
	defer e.mu.Unlock()

	e.state = nil
	path := filepath.Join(e.dataDir, inceptionStateFile)
	if err := os.Remove(path); err != nil && !os.IsNotExist(err) {
		return fmt.Errorf("removing state file: %w", err)
	}

	// Don't clear wiki on reset — keep last inception's facts visible
	// in the KB until a new inception writes new facts (writeFactsToVault
	// clears before writing).

	e.logger.Info("inception reset")
	return nil
}

// FormatAnswersForPrompt renders user answers as markdown for injection into
// the guide agent's kick template via ${INCEPTION_ANSWERS}.
func (e *InceptionEngine) FormatAnswersForPrompt() string {
	e.mu.Lock()
	defer e.mu.Unlock()

	if e.state == nil || len(e.state.Answers) == 0 {
		return ""
	}

	var b strings.Builder
	b.WriteString("# User Answers\n\n")
	for _, q := range e.state.Questions {
		answer, ok := e.state.Answers[q.ID]
		if !ok {
			continue
		}
		fmt.Fprintf(&b, "**%s**\n%s\n\n", q.Text, answer)
	}
	return b.String()
}

// --- persistence ---

func (e *InceptionEngine) loadState() {
	path := filepath.Join(e.dataDir, inceptionStateFile)
	data, err := os.ReadFile(path)
	if err != nil {
		return
	}
	var state InceptionState
	if err := json.Unmarshal(data, &state); err != nil {
		e.logger.Warn("corrupt inception state", "path", path, "error", err)
		return
	}
	e.state = &state
	e.logger.Info("inception state loaded",
		"phase", state.Phase,
		"mode", state.Mode,
	)
}

func (e *InceptionEngine) saveState() error {
	path := filepath.Join(e.dataDir, inceptionStateFile)
	dir := filepath.Dir(path)
	if err := os.MkdirAll(dir, 0o755); err != nil {
		return fmt.Errorf("creating state dir: %w", err)
	}

	data, err := json.MarshalIndent(e.state, "", "  ")
	if err != nil {
		return fmt.Errorf("marshaling state: %w", err)
	}

	return os.WriteFile(path, data, 0o644)
}

